package com.inmobia.vouchersystem;
public class Voucher extends Entry{

	
	private int prize_id_fk;
	private int promotion_id_fk;
	private boolean winning;
	private boolean used;
	private String timeStampUsed;
	public int getPrize_id_fk() {
		return prize_id_fk;
	}
	public int getPromotion_id_fk() {
		return promotion_id_fk;
	}
	public boolean isWinning() {
		return winning;
	}
	public boolean isUsed() {
		return used;
	}
	public String getTimeStampUsed() {
		return timeStampUsed;
	}
	public void setPrize_id_fk(int prize_id_fk) {
		this.prize_id_fk = prize_id_fk;
	}
	public void setPromotion_id_fk(int promotion_id_fk) {
		this.promotion_id_fk = promotion_id_fk;
	}
	public void setWinning(boolean winning) {
		this.winning = winning;
	}
	public void setUsed(boolean used) {
		this.used = used;
	}
	public void setTimeStampUsed(String timeStampUsed) {
		this.timeStampUsed = timeStampUsed;
	}
	@Override
	public String toString() {
		return "Voucher [prize_id_fk=" + prize_id_fk + ", promotion_id_fk="
				+ promotion_id_fk + ", winning=" + winning + ", used=" + used
				+ ", timeStampUsed=" + timeStampUsed + ", getId()=" + getId()
				+ ", getMsisdn()=" + getMsisdn() + ", getCmp_txid_fk()="
				+ getCmp_txid_fk() + ", getTimeStampAwarded()="
				+ getTimeStampAwarded() + ", toString()=" + super.toString()
				+ "]";
	}
	
	
	
	
}
