package com.inmobia.axiata.reports;

public class ReportDTO {

	private String timeAwarded;
	private int count;
	private int store_id_fk;
	private String store_name;
	private double prize_value_claimed;
	private double prize_value_unclaimed;
	private int claimed;
	
	public String getTimeAwarded() {
		return timeAwarded;
	}
	public int getCount() {
		return count;
	}
	public int getStore_id_fk() {
		return store_id_fk;
	}
	public String getStore_name() {
		return store_name;
	}
	
	public int getClaimed() {
		return claimed;
	}
	public void setTimeAwarded(String timeAwarded) {
		this.timeAwarded = timeAwarded;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public void setStore_id_fk(int store_id_fk) {
		this.store_id_fk = store_id_fk;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	
	public void setClaimed(int claimed) {
		this.claimed = claimed;
	}
	public double getPrize_value_claimed() {
		return prize_value_claimed;
	}
	public double getPrize_value_unclaimed() {
		return prize_value_unclaimed;
	}
	public void setPrize_value_claimed(double prize_value_claimed) {
		this.prize_value_claimed = prize_value_claimed;
	}
	public void setPrize_value_unclaimed(double prize_value_unclaimed) {
		this.prize_value_unclaimed = prize_value_unclaimed;
	}
	@Override
	public String toString() {
		return "ReportDTO [timeAwarded=" + timeAwarded + ", count=" + count
				+ ", store_id_fk=" + store_id_fk + ", store_name=" + store_name
				+ ", prize_value_claimed=" + prize_value_claimed
				+ ", prize_value_unclaimed=" + prize_value_unclaimed
				+ ", claimed=" + claimed + "]";
	}
	

}
