package com.inmobia.celcom.subscription.dto;

public class SubscriptionDTO {

	private int id;
	private String subscription_status;
	private int sms_service_id_fk;
	private String msisdn;
	private String subscription_timeStamp;
	private int smsmenu_levels_id_fk;
	
	public int getId() {
		return id;
	}
	public String getSubscription_status() {
		return subscription_status;
	}
	public int getSms_service_id_fk() {
		return sms_service_id_fk;
	}
	public String getMsisdn() {
		return msisdn;
	}
	public String getSubscription_timeStamp() {
		return subscription_timeStamp;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setSubscription_status(String subscription_status) {
		this.subscription_status = subscription_status;
	}
	public void setSms_service_id_fk(int sms_service_id_fk) {
		this.sms_service_id_fk = sms_service_id_fk;
	}
	public void setMsisdn(String msisdn) {
		this.msisdn = msisdn;
	}
	public void setSubscription_timeStamp(String subscription_timeStamp) {
		this.subscription_timeStamp = subscription_timeStamp;
	}
	public int getSmsmenu_levels_id_fk() {
		return smsmenu_levels_id_fk;
	}
	public void setSmsmenu_levels_id_fk(int smsmenu_levels_id_fk) {
		this.smsmenu_levels_id_fk = smsmenu_levels_id_fk;
	}
	@Override
	public String toString() {
		return "SubscriptionDTO [id=" + id + ", subscription_status="
				+ subscription_status + ", sms_service_id_fk="
				+ sms_service_id_fk + ", msisdn=" + msisdn
				+ ", subscription_timeStamp=" + subscription_timeStamp
				+ ", smsmenu_levels_id_fk=" + smsmenu_levels_id_fk + "]";
	}
	
	
	
	
	
	

}
